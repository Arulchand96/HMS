package entity;

public class Medicine {

   /*  */


    private String med_name;
    private String med_comp;
    private String exp_date;
    private String med_cost;
    private int count;







    public String getMed_name() {
        return med_name;
    }

    public void setMed_name(String med_name) {
        this.med_name = med_name;
    }

    public String getMed_comp() {
        return med_comp;
    }

    public void setMed_comp(String med_comp) {
        this.med_comp = med_comp;
    }

    public String getExp_date() {
        return exp_date;
    }

    public void setExp_date(String exp_date) {
        this.exp_date = exp_date;
    }

    public String getMed_cost() {
        return med_cost;
    }

    public void setMed_cost(String med_cost) {
        this.med_cost = med_cost;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
